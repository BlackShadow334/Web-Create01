wheel.innerHTML="<div style='height:0em;width:0em;animation:ani "+time+"s ease-in-out infinite;'></div>";

//strt..
    var di=0;
    var ti=0;
for(i=1;i<=numb;i++){
    wheel.innerHTML+="<div style='animation:ani "+time+"s ease-in-out infinite;'></div>";
    
    var h= heig/1 +di/1;
    var w= widt/1 + di/1;
    var t= 0.1 + ti/1;
    n=i+1;
    $("#wheel div:nth-child("+n+")").css({
        "height":h+"em",
        "width":w+"em",
        "animation":"ani "+time+"s ease-in-out infinite",
        "animation-delay":t+"s"
    });
    di= di/1 + dint/1;
    ti= ti/1 + tint/1;

//foe wheel
wheel.style="transform:rotateX("+view+"deg);animation:whole "+wtim+"s ease infinite;";

var whole= document.styleSheets[0].cssRules[2];
whole.deleteRule("100%");
whole.appendRule("100%{transform:rotateY("+wviw+"deg)}");
//wheel end

};

